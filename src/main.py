def game_over(win):
    win.clear()
    curses.init_pair(1, curses.COLOR_RED, curses.COLOR_BLACK)
    RED_AND_BLACK = curses.color_pair(1)

    gameOver = "GAME OVER!!"
    y,x = win.getmaxyx()

    y= y//2
    x = x//2-len(gameOver)
    i = 5
    while i < 10:
        win.refresh()

        win.addstr(y,x, gameOver)

        win.refresh()
        curses.napms(1000)

        win.attron(RED_AND_BLACK)
        win.addstr(y,x, gameOver)
        win.attroff(RED_AND_BLACK)

        win.refresh()
        curses.napms(1000)
        i += 1


